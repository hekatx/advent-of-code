const fs = require("fs");

// read input
let input = fs
  .readFileSync("input.txt", "utf8")
  .split(/\n/)
  .filter((e) => e !== "");

///////////////////////////////////////////////////////////////////////////////////////////////////
// First part
///////////////////////////////////////////////////////////////////////////////////////////////////

let res = "";

let j = 0;

for (let i = 0; i < input[0].length; i++) {
  if (res.length === 4) {
    j = i;
    break;
  }
  let char = input[0][i];
  if (!res.includes(char)) {
    res += char;
  } else {
    res = char;
  }
}

console.log(res, j);

///////////////////////////////////////////////////////////////////////////////////////////////////
// Second part
///////////////////////////////////////////////////////////////////////////////////////////////////

let res_2 = "";

let k = 0;

for (let i = 0; i < input[0].length; i++) {
  if (res_2.length === 14) {
    k = i;
    break;
  }
  let char = input[0][i];
  if (!res_2.includes(char)) {
    res_2 += char;
  } else {
    res_2 = char;
  }
}

console.log(res_2, k);

